CREATE TABLE IF NOT EXISTS grades (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS users (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    pseudo TEXT NOT NULL UNIQUE,
    password TEXT NOT NULL,
    admin BOOLEAN NOT NULL DEFAULT FALSE,
    salary_percentage NUMERIC(5,2),
    group_share_percentage NUMERIC(5,2),
    grade INTEGER REFERENCES grades(id) ON DELETE SET NULL
);

CREATE TABLE IF NOT EXISTS player_stats (
    user_id INTEGER PRIMARY KEY REFERENCES users(id) ON DELETE CASCADE,
    total_money NUMERIC(12,2) NOT NULL DEFAULT 0,
    total_drugs_sold NUMERIC(12,2) NOT NULL DEFAULT 0,
    total_heists INTEGER NOT NULL DEFAULT 0
);

CREATE TABLE IF NOT EXISTS heist_types (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL UNIQUE,
    money_type TEXT NOT NULL DEFAULT 'Sale'
);

CREATE TABLE IF NOT EXISTS drug_types (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS heists_history (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    heist_name TEXT,
    city TEXT,
    heist_type TEXT NOT NULL,
    money_type TEXT NOT NULL DEFAULT 'Sale',
    participants JSONB NOT NULL DEFAULT '[]'::jsonb,
    weapon TEXT,
    gain NUMERIC(12,2) NOT NULL DEFAULT 0,
    heist_date TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS drug_sales_history (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    drug_type TEXT NOT NULL,
    quantity NUMERIC(12,2) NOT NULL DEFAULT 0,
    revenue NUMERIC(12,2) NOT NULL DEFAULT 0,
    sale_date TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS players (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL UNIQUE,
    total_money NUMERIC(12,2) NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS activities (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    player_id INTEGER NOT NULL REFERENCES players(id) ON DELETE CASCADE,
    type TEXT NOT NULL,
    quantity NUMERIC(12,2) NOT NULL DEFAULT 0,
    gain NUMERIC(12,2) NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

